    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        String idCargo = cbIdCargo.getSelectedItem().toString();
        String idUsuario = cbIdUsuario.getSelectedItem().toString();
        Calendar calendar;
        int dia, mes, anio;
        calendar = dcFechaInicio.getCalendar();
        dia = calendar.get(Calendar.DAY_OF_MONTH);
        mes = calendar.get(Calendar.MONTH);
        anio = calendar.get(Calendar.YEAR) - 1900;
        Date fechaInicio = new Date(anio, mes, dia);
        CCargoHis ccargohis = new CCargoHis(idCargo, idUsuario, fechaInicio);
        MCargoHis mcargohis = new MCargoHis();

        try {    
            mcargohis.insertarCargoHis(ccargohis);
            JOptionPane.showMessageDialog(rootPane, "Registro exitoso!!!","Registro hist贸rico cargo",JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
            System.out.println("Ocurri贸 un error mientras se intentaba ingresar el hist贸rico cargo!!!");
        }
       
    }//GEN-LAST:event_btnRegistrarActionPerformed

