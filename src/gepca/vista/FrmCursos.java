    private void btnCursoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCursoBuscarActionPerformed
        String idCurso = txtCursoBuscar.getText().trim();
        if (!idCurso.equals("")){
            DefaultTableModel modelo;
            MCurso mcurso = new MCurso();
            modelo = mcurso.buscarCurso(idCurso);
            if(modelo.getRowCount() >= 1){
                System.out.println("Curso con id " + idCurso + " encontrado!!!");
                tablaCurso.setModel(modelo);
                Integer cantCursos = tablaCurso.getRowCount();
                lblCursoTotal.setText(String.valueOf(cantCursos));
                limpiar();
            }else{
                JOptionPane.showMessageDialog(rootPane, "El curso " + idCurso + " no se encontró!!!","Busqueda de curso",JOptionPane.INFORMATION_MESSAGE);
                txtCursoBuscar.requestFocus();
                mostrarCursos();
                limpiar();
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "El curso " + idCurso + " no se encontró!!!","Busqueda de curso",JOptionPane.INFORMATION_MESSAGE);
            txtCursoBuscar.requestFocus();
            mostrarCursos();
            limpiar();
        }
    }//GEN-LAST:event_btnCursoBuscarActionPerformed

