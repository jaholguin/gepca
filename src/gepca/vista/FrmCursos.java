    public void mostrarCursos() {
        try {
            DefaultTableModel modelo;
            MCurso mcurso = new MCurso();
            modelo = mcurso.consultarCursos();
            tablaCurso.setModel(modelo);
            Integer cantCursos = tablaCurso.getRowCount();
            lblCursoTotal.setText(String.valueOf(cantCursos));
	} catch (Exception e) {
            System.out.println("Error al consultar la BD!!!" + e);
	}
    }

    public void limpiar() {
        txtCodCurso.setEnabled(false);
        txtNombreCurso.setEnabled(false);
        txtTituloCurso.setEnabled(false);
        btnCursoNuevo.setEnabled(true);
        btnCursoCancelar.setEnabled(false);
        btnCursoGuardar.setEnabled(false);
        txtCodCurso.setText("");
        txtNombreCurso.setText("");
        txtTituloCurso.setText("");
        txtCursoBuscar.setText("");
        txtCursoBuscar.requestFocus();
    }

    public void eliminarCurso() {
        
        String idCurso = tablaCurso.getValueAt(row, 0).toString();
        int confirmation = JOptionPane.showConfirmDialog(rootPane, "Estás seguro de eliminar el curso con id " + idCurso + "?","Confirmar",2);

        if (confirmation == 0) {
            MCurso mcurso = new MCurso();
            mcurso.eliminarCurso(idCurso);
            JOptionPane.showMessageDialog(rootPane, "Curso: " + idCurso + " borrado exitosamente!!!","Eliminación de curso",JOptionPane.INFORMATION_MESSAGE);
        }
    }
