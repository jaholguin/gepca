    private void btnCursoGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCursoGuardarActionPerformed
        String idCurso = txtCodCurso.getText();
        String nombre = txtNombreCurso.getText();
        String titulo = txtTituloCurso.getText();
        if (idCurso.trim().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar un código","Registro de curso",JOptionPane.INFORMATION_MESSAGE);
            txtCodCurso.requestFocus();
            return;
        }
        if (nombre.trim().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar un nombre","Registro de curso",JOptionPane.INFORMATION_MESSAGE);
            txtNombreCurso.requestFocus();
            return;
        }
        if (titulo.trim().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar un título","Registro de titulo",JOptionPane.INFORMATION_MESSAGE);
            txtTituloCurso.requestFocus();
            return;
        }
        CCurso curso = new CCurso(idCurso, nombre, titulo);
        MCurso mcurso = new MCurso();
        if (action.equals("save")) {
            try {    
                mcurso.insertarCurso(curso);
                mostrarCursos();
                limpiar();
            } catch (Exception e) {
                JOptionPane.showConfirmDialog(null, e);
                System.out.println("Ocurrió un error mientras se intentaba ingresar el curso!!!");
            }
        } else {
            if (action.equals("edit")) {
                try {
                    mcurso.editarCurso(curso);
                    mostrarCursos();
                    limpiar();
                } catch (Exception e) {
                    JOptionPane.showConfirmDialog(null, e);
                    System.out.println("Ocurrió un error mientras se intentaba editar el curso!!!");
                }
            }
        } 
        
    }//GEN-LAST:event_btnCursoGuardarActionPerformed
    
