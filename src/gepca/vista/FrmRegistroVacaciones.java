    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        String idUsuario = cbIdUsuario.getSelectedItem().toString();
        Calendar calendarInicio;
        int diaInicio, mesInicio, anioInicio;
        calendarInicio = dcFechaInicio.getCalendar();
        diaInicio = calendarInicio.get(Calendar.DAY_OF_MONTH);
        mesInicio = calendarInicio.get(Calendar.MONTH);
        anioInicio = calendarInicio.get(Calendar.YEAR) - 1900;
        Date fechaInicio = new Date(anioInicio, mesInicio, diaInicio);
        Calendar calendarFin;
        int diaFin, mesFin, anioFin;
        calendarFin = dcFechaFin.getCalendar();
        diaFin = calendarFin.get(Calendar.DAY_OF_MONTH);
        mesFin = calendarFin.get(Calendar.MONTH);
        anioFin = calendarFin.get(Calendar.YEAR) - 1900;
        Date fechaFin = new Date(anioFin, mesFin, diaFin);
        String duracion = lblDifFechas.getText();
        CVacaciones cvacaciones = new CVacaciones(fechaInicio, duracion, fechaFin, idUsuario);
        MVacaciones mvacaciones = new MVacaciones();
        MUsuario musuario = new MUsuario();

        try {    
            mvacaciones.insertarVacaciones(cvacaciones);
            musuario.editarUsuario("Vacaciones", idUsuario);
            JOptionPane.showMessageDialog(rootPane, "Registro exitoso!!!","Registro vacaciones",JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
            System.out.println("Ocurrió un error mientras se intentaba ingresar el histórico cargo!!!");
        }
       
    }//GEN-LAST:event_btnRegistrarActionPerformed

