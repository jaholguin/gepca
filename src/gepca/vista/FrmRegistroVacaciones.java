    public void cargarUsuarios() {
        try {
            DefaultComboBoxModel modelo;
            MUsuario musuarios = new MUsuario();
            modelo = musuarios.cargarUsuarios();
            cbIdUsuario.setModel(modelo);
	} catch (Exception e) {
            System.out.println("Error al consultar la BD!!!" + e);
	}
    }
    

